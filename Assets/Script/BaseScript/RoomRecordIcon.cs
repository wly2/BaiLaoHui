public class RoomRecordIcon
{
    public string playerImage;
    public string playerName;
    public int playCount;
    public string integral;

    public RoomRecordIcon()
    {
    }

    public RoomRecordIcon(string pi, string pn, int pc, string i)
    {
        playerImage = pi;
        playerName = pn;
        playCount = pc;
        integral = i;
    }
}