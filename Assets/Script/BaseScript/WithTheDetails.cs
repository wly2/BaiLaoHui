public class WithTheDetails
{
    public string playerName;
    public string type;
    public string zimo;
    public string integral;

    public WithTheDetails()
    {
    }

    public WithTheDetails(string pn, string t, string z, string i)
    {
        this.playerName = pn;
        this.type = t;
        this.zimo = z;
        this.integral = i;
    }
}